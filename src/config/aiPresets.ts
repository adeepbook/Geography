// ---------------------------------------------------------------------------
// 注入函数
// ---------------------------------------------------------------------------

export interface PresetContext {
  地名: string;
  lat: string;
  lng: string;
  location?: LocationScript;
}

function buildCardContent(loc: LocationScript): string {
  let s = '\n\n【参考资料·精选地点】\n';
  s += `类型：${loc.类型标签.join('、')}\n`;
  s += `简介：${loc.一句话}\n`;
  if (loc.区位) s += `区位：${loc.区位}\n`;
  if (loc.形成时间线.length) {
    s += `形成过程：${loc.形成时间线.map((e) => e.事件).join(' → ')}\n`;
  }
  const layers = [
    ...loc.垂直拆解.天上,
    ...loc.垂直拆解.地面,
    ...loc.垂直拆解.地下,
  ].filter(Boolean);
  if (layers.length) s += `垂直结构：${layers.join('、')}\n`;
  if (loc.本地注解) s += `注解：${loc.本地注解}\n`;
  s += `（内容状态：${loc.状态}）`;
  return s;
}

export function injectPreset(template: string, ctx: PresetContext): string {
  const cardContent = ctx.location ? buildCardContent(ctx.location) : '';
  return template
    .replace(/{地名}/g, ctx.地名)
    .replace(/{lat}/g, ctx.lat)
    .replace(/{lng}/g, ctx.lng)
    .replace(/{卡片内容}/g, cardContent);
}
